    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

     
            // display the selected row data in the jTextfields
            
            //get the jtabel model
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            //get the selected row index
            int rindex = jTable1.getSelectedRow();
            
            //display data
            jTextFieldID.setText(model.getValueAt(rindex, 0).toString());
            jTextFieldClientId.setText(model.getValueAt(rindex, 1).toString());
            jTextFieldRoomNo.setText(model.getValueAt(rindex, 2).toString());
            
          try {
            // display the dateIn and dateOut from jTable to jdatechooser
            Date dateIn = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rindex, 3).toString());
            jDateChooserDateIn.setDate(dateIn);
            
             Date dateOut = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rindex, 3).toString());
            jDateChooserDateOut.setDate(dateOut);
        } catch (ParseException ex) {
            Logger.getLogger(ManageReservations.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed

       // edit the selected reservation
        
        //get data from the fields
      
       
        
        
            try{
                 int id = Integer.valueOf(jTextFieldID.getText());
                 int room_number = Integer.valueOf(jTextFieldRoomNo.getText());
                 int client_id = Integer.valueOf(jTextFieldClientId.getText());
                 SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                 String dateIn = dateFormat.format(jDateChooserDateIn.getDate()) ;
                 String dateOut = dateFormat.format(jDateChooserDateIn.getDate());
        
                 
                if(reservation.editReservation(id, client_id, room_number, dateIn, dateOut))
                {
           
                    JOptionPane.showMessageDialog(rootPane, "Reservation Information Updated Successfully", "Edit Reservation", JOptionPane.INFORMATION_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Reservation Information Not Updated ", "Edit Reservation Error", JOptionPane.ERROR_MESSAGE);
                }
               }
               catch(NumberFormatException ex){
                
                JOptionPane.showMessageDialog(rootPane, "Enter the room number + Client Id + Rservation Id", "Data error", JOptionPane.ERROR_MESSAGE);
               }
               catch(NullPointerException ex){
                
                JOptionPane.showMessageDialog(rootPane, "Select the Date In and Date Out", "Date error", JOptionPane.ERROR_MESSAGE);
               }
            
            
        
        

    }//GEN-LAST:event_jButtonEditActionPerformed


    private void jButtonAddReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddReservationActionPerformed

         try{
             int client_id = Integer.valueOf(jTextFieldClientId.getText());
             int room_number = Integer.valueOf(jTextFieldRoomNo.getText());
             SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
             String date_in = dateFormat.format(jDateChooserDateIn.getDate()) ;
             String date_out = dateFormat.format(jDateChooserDateIn.getDate());
        
        if(reservation.addReservation(client_id,room_number, date_in,date_out))
        {
            
            JOptionPane.showMessageDialog(rootPane, "New Reservation Added Successfully", "New Reservation Added Successfully", JOptionPane.INFORMATION_MESSAGE);
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "Reservation Not Added", "Reservation error", JOptionPane.ERROR_MESSAGE);
        }
    }
    catch(NumberFormatException ex){
        JOptionPane.showMessageDialog(rootPane,"Enter Your Room number + Client ID", "Input Fields Error", JOptionPane.ERROR_MESSAGE);
    }
     
    }//GEN-LAST:event_jButtonAddReservationActionPerformed

